        public void llenar(xmlClientes xmlC){
        String[] columna = {  "   Nombre    ","   Apellido   ","   CÃ©dula   ","   Tipo V/N    ","   Rif   ","  Direccion   ","   Telefono   "};
        DefaultTableModel dtm = new DefaultTableModel(null,columna);
        ArrayList<Clientes> Lista = xmlC.todosLosClientes();
        for (Clientes cl : Lista) {
          String[] row = {cl.getNombre(), cl.getApellido(), String.valueOf(cl.getCi()), cl.getTipoV(), String.valueOf(cl.getRif()), cl.getDireccion(), String.valueOf(cl.getTlf())};
          dtm.addRow(row);
         }
         TablaClientes.setModel(dtm);
        
        
    }
