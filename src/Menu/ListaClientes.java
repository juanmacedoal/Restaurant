    private void BtnModificarMActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarMActionPerformed
        // TODO add your handling code here:
        String nombre,apellido, cedula,direccion,tlf,rif,tipoV,clienteE;
        int fselc= TablaClientes.getSelectedRow();
        if (fselc==-1){
              JOptionPane.showMessageDialog(null,"Debe seleccionar un empleado","Advertencia",JOptionPane.WARNING_MESSAGE);
          }else{
              nombre =TxtNombreM.getText();
              apellido =TxtApellidoM.getText();
              cedula =TxtCedulaM.getText();
              tlf =TxtTlfM.getText();
              direccion =TxtDireccionM.getText();
              tipoV= TxtTipoVM.getSelectedItem().toString();
              rif =TxtRifM.getText();
              dtm =(DefaultTableModel)TablaClientes.getModel();
              clienteE = TablaClientes.getValueAt(fselc,2).toString();
              cliE = new Clientes(clienteE);
              dtm.setValueAt(TxtNombreM.getText(), TablaClientes.getSelectedRow(), 0);
              dtm.setValueAt(TxtApellidoM.getText(), TablaClientes.getSelectedRow(), 1);
              dtm.setValueAt(TxtCedulaM.getText(), TablaClientes.getSelectedRow(), 2);
              dtm.setValueAt(TxtDireccionM.getText(), TablaClientes.getSelectedRow(), 5);
              dtm.setValueAt(TxtTlfM.getText(), TablaClientes.getSelectedRow(), 6);
              dtm.setValueAt(TxtRifM.getText(), TablaClientes.getSelectedRow(), 4);
              dtm.setValueAt(TxtTipoVM.getSelectedItem().toString(), TablaClientes.getSelectedRow(), 3);
             
            
              cli = new Clientes(rif, tlf, cedula, tipoV, nombre, apellido, direccion);
              xmlC.actualizarPersona(cliE,cli);
              ModificarCliente.dispose();
        
        }
       

    }//GEN-LAST:event_BtnModificarMActionPerformed

