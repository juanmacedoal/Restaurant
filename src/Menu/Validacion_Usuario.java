    private void validarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validarActionPerformed
        // TODO add your handling code here:
        Controlador control;
        control = new Controlador(clave);
        int numero = control.contrasena(clave);
        
        if(numero == 1){
            gerente ge = new gerente();
            ge.setLocationRelativeTo(null);
            ge.setVisible(true);
            this.dispose();
            
        }
        
        if(numero == 2){
            Mesas me = new Mesas(xmlE);
            me.setLocationRelativeTo(null);
            me.setVisible(true);
            this.dispose();
            
        }
        
         if(numero == 3){
            cajeros ca = new cajeros(xmlC);
            ca.setLocationRelativeTo(null);
            ca.setVisible(true);
            this.dispose();
            
        }
         
        if(numero == 4){
            Error er = new Error();
            er.setLocationRelativeTo(null);
            er.setVisible(true);
            this.dispose();
            
        }
        
    }//GEN-LAST:event_validarActionPerformed

