    private void BtnAgregarClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BtnAgregarClienteKeyPressed
        // TODO add your handling code here:
       
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
        BuscaCliente.setSize(700, 250);
        BuscaCliente.setLocationRelativeTo(null);
        BuscaCliente.setModal(true);
        BuscaCliente.setVisible(true);
        llenar(xmlC);
        setTitle("LOMAC");
        BuscaCliente.setIconImage(new ImageIcon("src/imagenes/Restaurant.png").getImage());
        BotonImprimir.setEnabled(true);
         }
         if(BotonImprimir.isEnabled()==false) 
            if(evt.getKeyCode() == KeyEvent.VK_RIGHT) {
                 BotonAtras.requestFocus(); 
         }
         if(BotonImprimir.isEnabled()==true) 
          if(evt.getKeyCode() == KeyEvent.VK_RIGHT) {
          BotonCobrar.requestFocus();
         }
    }//GEN-LAST:event_BtnAgregarClienteKeyPressed

