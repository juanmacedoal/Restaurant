    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        // TODO add your handling code here:
        boolean resultado = false;
       try{
         
         if(producto.getText().isEmpty()  || (int)cantidad.getValue() <= 0)
            JOptionPane.showMessageDialog(null,"Ingreso mal los datos","Advertencia",JOptionPane.WARNING_MESSAGE);
         else{
           InventarioN = new Inventarios();
           Controlador control;
           control = new Controlador(producto, cantidad);
           control.datosInventario(InventarioN, res); 
           resultado = xmlI.agregarInventario(InventarioN);
           control = new Controlador(agregar);  
           control.activa_Desactiva(true);
           control.inicializarTxt(producto, cantidad);
         }
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,"Ingreso mal los datos","Advertencia",JOptionPane.WARNING_MESSAGE);
       }
     
        
    }//GEN-LAST:event_agregarActionPerformed

