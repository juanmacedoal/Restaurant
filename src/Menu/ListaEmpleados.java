    public void llenar(xmlEmpleado xmlE){
        
        String[] columna = {  "   Nombre    ","   Apellido   ","   Cédula   ","   Direccion   ","   Dia   ","   Mes   ","   Año   ", "   Tlf-Cel   ", "   Tlf-Casa   ", "  Cargo  "};
        DefaultTableModel dtm = new DefaultTableModel(null,columna);
        ArrayList<Mesoneros> Lista = xmlE.todosLosMesoneros();
        for (Mesoneros mes : Lista){
          String[] row = {mes.getNombre(), mes.getApellido(), mes.getCi(), mes.getDireccion(), mes.getDia(), mes.getMes(), mes.getAno(), mes.getCel(), mes.getCasa(), mes.getCargo()};
          dtm.addRow(row);
         }
         tablaMesoneros.setModel(dtm);
        
        
    }
