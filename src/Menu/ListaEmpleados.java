    private void BtnModificarMKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BtnModificarMKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode() == KeyEvent.VK_UP) {
         TxtCargoM.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_RIGHT) {
         BtnAtrasM.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
           String nombre,apellido, cedula,direccion,dia,mes,ano, tlfCell, tlfCasa, cargo, empleadoE;
        int fselc= tablaMesoneros.getSelectedRow();
        if (fselc==-1){
              JOptionPane.showMessageDialog(null,"Debe seleccionar un empleado","Advertencia",JOptionPane.WARNING_MESSAGE);
          }else{
              nombre =TxtNombreM.getText();
              apellido =TxtApellidoM.getText();
              cedula =TxtCedulaM.getText();
              direccion =TxtDireccionM.getText();
              dia= TxtDiaM.getSelectedItem().toString();
              mes= TxtMesM.getSelectedItem().toString();
              ano= TxtAnoM.getSelectedItem().toString();
              tlfCell =TxtTlfCellM.getText();
              tlfCasa =TxtTlfCasaM.getText();
              cargo =TxtCargoM.getSelectedItem().toString();
              dtm =(DefaultTableModel)tablaMesoneros.getModel();
              empleadoE = tablaMesoneros.getValueAt(fselc,2).toString();
              empE = new Mesoneros(empleadoE);
              dtm.setValueAt(TxtNombreM.getText(), tablaMesoneros.getSelectedRow(), 0);
              dtm.setValueAt(TxtApellidoM.getText(), tablaMesoneros.getSelectedRow(), 1);
              dtm.setValueAt(TxtCedulaM.getText(), tablaMesoneros.getSelectedRow(), 2);
              dtm.setValueAt(TxtDireccionM.getText(), tablaMesoneros.getSelectedRow(), 3);
              dtm.setValueAt(TxtDiaM.getSelectedItem().toString(), tablaMesoneros.getSelectedRow(), 4);
              dtm.setValueAt(TxtMesM.getSelectedItem().toString(), tablaMesoneros.getSelectedRow(), 5);
              dtm.setValueAt(TxtAnoM.getSelectedItem().toString(), tablaMesoneros.getSelectedRow(), 6);
              dtm.setValueAt(TxtTlfCellM.getText(), tablaMesoneros.getSelectedRow(), 7);
              dtm.setValueAt(TxtTlfCasaM.getText(), tablaMesoneros.getSelectedRow(), 8);
              dtm.setValueAt(TxtCargoM.getSelectedItem().toString(), tablaMesoneros.getSelectedRow(), 9);
             
            
              emp = new Mesoneros(dia, mes, ano, cedula, tlfCell, tlfCasa, nombre, apellido, direccion, cargo);
              xmlE.actualizarPersona(empE,emp);
              ModificaEmpleado.dispose();
        
        }
      }
    }//GEN-LAST:event_BtnModificarMKeyPressed

