    private void BtnElimEmpleadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_BtnElimEmpleadoKeyPressed
        // TODO add your handling code here:
        
        if(evt.getKeyCode() == KeyEvent.VK_RIGHT) {
         BtnModifEmpleado.requestFocus();
        }
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {  
        String eliminarE;
        int fselc= tablaMesoneros.getSelectedRow();
        if (fselc==-1){
              JOptionPane.showMessageDialog(null,"Debe seleccionar un empleado","Advertencia",JOptionPane.WARNING_MESSAGE);
          }else{
                dtm =(DefaultTableModel)tablaMesoneros.getModel();
                eliminarE = tablaMesoneros.getValueAt(fselc,2).toString();
                 emp = new Mesoneros(eliminarE);
                xmlE.borrarPersona(emp);
                dtm.removeRow(fselc);
        }
        }
    }//GEN-LAST:event_BtnElimEmpleadoKeyPressed

